#include <stdlib.h>
#include "twl_argparser/argparser.h"

void				argparser_del(t_argparser *this)
{
	free(this->name);
	twl_lst_del(this->arguments, argparser_argument_del_void);
	if (this->usage_text)
		free(this->usage_text);
	free(this);
}
